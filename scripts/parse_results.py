#!/usr/bin/env python3
import csv
from pathlib import Path


RESULTS_DIR = Path("results")
LOG = RESULTS_DIR / "sim_log.csv"
REPORT = RESULTS_DIR / "simulation_report.txt"


def main():
    if not LOG.exists():
        raise SystemExit("results/sim_log.csv not found. Run make test first.")

    with LOG.open(newline="") as f:
        rows = list(csv.DictReader(f))

    passed = sum(1 for row in rows if row["status"] == "PASS")
    total = len(rows)

    lines = [
        "RISC-V RV32I subset CPU simulation report",
        "==========================================",
        "",
        f"Integration tests passed: {passed}/{total}",
        "",
        "Test results:",
    ]

    for row in rows:
        lines.append(
            f"- {row['test']}: {row['status']} "
            f"({row['cycles']} cycles, {row['program']})"
        )

    lines.extend(
        [
            "",
            "Notes:",
            "- Unit test PASS/FAIL lines are printed by vvp during make test.",
            "- results/waveform.vcd is generated by the integration testbench.",
        ]
    )

    REPORT.write_text("\n".join(lines) + "\n")
    print(REPORT.read_text(), end="")

    if passed != total:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
